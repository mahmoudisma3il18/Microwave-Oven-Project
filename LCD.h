#ifndef LCD_H_
#define LCD_H_

#include "common_macros.h"
#include "TM4C123.h"
#include "systick.h"
#include "gpio.h"

/***********************************************************************************
                       Preprocessor Macros                                    
 **********************************************************************************/

/************************ LCD Data bits mode configuration ************************/



/******************************** LCD HW Pins **************************************/
#define RS 0x01   //PIN0 IN PORTB
#define RW 0x02   //PIN1 IN PORTB
#define EN 0x04   //PIN2 IN PORTB

#define LCD (GPIOB->DATA)

/*************************************************************************************
D7 -----> PB7
D6 -----> PB6
D5 -----> PB5
D4 -----> PB4
**************************************************************************************/


/*************************************************************************************
                                   LCD Commands 
*************************************************************************************/
#define CLEAR_COMMAND 0x01
#define FOUR_BITS_DATA_MODE 0x02
#define TWO_LINE_LCD_Four_BIT_MODE 0x28
#define CURSOR_OFF 0x0C
#define CURSOR_ON 0x0E
#define SET_CURSOR_LOCATION 0x80 //Takes cursor to beginning of first row
#define INCREMENT_CURSOR 0x06
#define LCD_ON  0x0F

/*************************************************************************************
                       Functions Prototypes                                   
 ************************************************************************************/

/*************************************************************************************
                     Function to intialze the lcd
**************************************************************************************/

void LCD_Init(void);

/*************************************************************************************
                     Function Used to send 4 bits to lcd
**************************************************************************************/

void LCD_writeBits(uint8_t data, uint8_t control);

/*************************************************************************************
                     Function to send the command to lcd
**************************************************************************************/

void LCD_sendCommand(uint8_t command);

/*************************************************************************************
                     Function to display charater on lcd
**************************************************************************************/

void LCD_displayCharacter(uint8_t data);

/*************************************************************************************
                     Function to display string on lcd
**************************************************************************************/

void LCD_displayString(const uint8_t *Str);

/*************************************************************************************
                  Function to set cursor to the choosen location
**************************************************************************************/

void LCD_goToRowColumn(uint8_t row,uint8_t col);

/*************************************************************************************
                     Function to clear screen of lcd
**************************************************************************************/

void LCD_clearScreen(void);

/*************************************************************************************
                 Function to show time in mm:ss format
**************************************************************************************/

void LCD_displayTime(int16_t Time_In_Secs);

/*************************************************************************************
                 Function to show string in the specifed loaction
**************************************************************************************/

void LCD_displayStringRowColumn(uint8_t row,uint8_t col,const uint8_t *Str);

/*************************************************************************************
                     Function to print Integr on lcd
**************************************************************************************/

void LCD_PrintINT(uint32_t number);


void LCD_displayCharRowColumn(uint8_t row,uint8_t col, uint8_t data);

#endif /* LCD_H_ */